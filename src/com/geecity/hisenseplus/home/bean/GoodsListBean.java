package com.geecity.hisenseplus.home.bean;

import java.io.Serializable;

/**
 * 商户的商品列表类
 * 
 * @author Administrator
 * 
 */
public class GoodsListBean implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 4476217829726048468L;
    private int goods_id;
    private String goods_name;// 名称
    private String default_image;
    private double price;
    private double old_price;
    private int stock;
    private int views;
    private int sales;

    public int getGoods_id() {
        return goods_id;
    }

    public void setGoods_id(int goods_id) {
        this.goods_id = goods_id;
    }

    public String getGoods_name() {
        return goods_name;
    }

    public void setGoods_name(String goods_name) {
        this.goods_name = goods_name;
    }

    public String getDefault_image() {
        return default_image;
    }

    public void setDefault_image(String default_image) {
        this.default_image = default_image;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getOld_price() {
        return old_price;
    }

    public void setOld_price(double old_price) {
        this.old_price = old_price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public int getViews() {
        return views;
    }

    public void setViews(int views) {
        this.views = views;
    }

    public int getSales() {
        return sales;
    }

    public void setSales(int sales) {
        this.sales = sales;
    }

    @Override
    public String toString() {
        return "GoodsListBean [goods_id=" + goods_id + ", goods_name=" + goods_name
                        + ", default_image=" + default_image + ", price=" + price + ", old_price="
                        + old_price + ", stock=" + stock + ", views=" + views + ", sales=" + sales
                        + "]";
    }
}
